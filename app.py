course = "Python Programming"
print(len(course))
print(course[0])
# Shows the 2nd to last letter in the index since going negative
print(course[-2])
# Will show the first 3 from the 0 index to the 2 index (pyt)
print(course[:3])
# will show the whole length of the string
print(course[0:])
# Is the same as the line above
print(course[:])
