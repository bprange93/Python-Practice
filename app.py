# Short cut to run code Ctrl Alt n.
# Using the single quotes will allow you to use the double quote inside and print it.
# Backslash is a special character. It is known as an escape sequence
# \n means new line. If used in message it will show programming on the next line.
# This way seemed to have created a little indent because of the space when run.
message = "Python \n Programing"
# another cleaner way to do the same thing.
message2 = """
Python 
Programing
"""
print(message)
print(message2)
