# Short cut to run code Ctrl Alt n.
# Using upper case letters lets other developers know that this variable is a constant.
temperature = 15
# when using an if statment always terminate the expression with a ( : )
if temperature > 30:
    print("It's warm")
    print("Drink water")
elif temperature > 20:
    print("It's nice")
else:
    print("It's cold")
print("Done")
